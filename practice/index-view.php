        <h1>Slapjack Simulator</h1>

        <h2>Mechanics</h2>
        <ul>
            <li>Each player starts with 26 cards--half of a shuffled standard
                deck.</li>
            <li>Each player alternates discarding one card into the pile.</li>
            <li>Once a jack is discarded, both players try to slap the jack
                first.</li>
            <li>The player who slaps the jack first gets to keep the entire
                discard pile.</li>
            <li>Play continues as above until one player is out of cards.</li>
            <li>If one player runs out of cards before the pile gets picked up,
                <br>
                the other player continues to discard until a jack is played.
            </li>
            <li>If the player who was out of cards is able to slap the jack <br>
                and pick up the pile, play continues.</li>
            <li>The player who accumulates 52 cards wins.</li>
        </ul>

